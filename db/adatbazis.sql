  CREATE OR REPLACE FORCE VIEW "SOCIAL"."VISMEROS" ("FELHASZNALO_ID", "ISMEROS_ID", "TIPUS") AS 
  select 
f.felhasznalo_id,
case when f.felhasznalo_id = fk.KEZDEMENYEZO_FELHASZNALO_ID then fk.CEL_FELHASZNALO_ID else fk.KEZDEMENYEZO_FELHASZNALO_ID end as ismeros_id,
fk.tipus
from
felhasznalo f
inner join felhasznalo_kapcsolat fk on f.felhasznalo_id = fk.KEZDEMENYEZO_FELHASZNALO_ID or f.FELHASZNALO_ID = fk.CEL_FELHASZNALO_ID;
