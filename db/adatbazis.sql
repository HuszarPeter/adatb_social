  CREATE OR REPLACE FORCE VIEW "SOCIAL"."VBEJEGYZES_CIMKE" ("BEJEGYZES_ID", "TAGEK") AS 
  select 
bejegyzes_id, LISTAGG('#' || nev, ', ') WITHIN GROUP (order by nev) as tagek
from(

select 
bejegyzes_cimke.BEJEGYZES_ID,
cimke.nev
from bejegyzes_cimke
inner join cimke on cimke.CIMKE_ID = bejegyzes_cimke.CIMKE_ID
)
group by BEJEGYZES_ID;
